// ============================================
/**
 * 步骤:
 * 1. 启动应用 (npm run dev 或 npm run tauri:dev)
 * 2. 在侧边栏的任意 .md 文件上右键点击
 *
 * 验证点:
 * - [ ] 菜单在光标位置显示
 * - [ ] 菜单标题显示 "File Actions"
 * - [ ] 菜单包含：Rename, Reveal in Finder, Delete 选项
 * - [ ] 菜单有阴影和圆角样式
 * - [ ] 悬停时菜单项有高亮效果
 */

