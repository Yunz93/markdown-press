export function normalizeMarkdownStylePreset(value: unknown): MarkdownStylePreset {
  return MARKDOWN_STYLE_PRESETS.includes(value as MarkdownStylePreset)
    ? value as MarkdownStylePreset
    : DEFAULT_MARKDOWN_STYLE_PRESET;
}

export function getMarkdownStyleTokens(
  preset: MarkdownStylePreset | undefined,
  themeMode: ThemeMode,
): MarkdownStyleTokens {
  return MARKDOWN_STYLE_TOKENS[normalizeMarkdownStylePreset(preset)][themeMode];
}

