#!/usr/bin/env node

import { mkdtempSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';

const RAW_COMMENT_PREFIX = 'untrusted comment:';

function fail(message) {
  console.error(message);
  process.exit(1);
}

function normalizeRawKey(value) {
  const trimmed = value.trim();
  if (!trimmed.startsWith(RAW_COMMENT_PREFIX)) {
    return null;
  }

  return trimmed.replace(/\\n/g, '\n');
}

function decodeBase64Key(value) {
  const normalized = value.replace(/\s+/g, '');
  if (!normalized) {
    return null;
  }

  try {
    const decoded = Buffer.from(normalized, 'base64').toString('utf8').trim();
    return decoded.startsWith(RAW_COMMENT_PREFIX) ? decoded : null;
  } catch {
    return null;
  }
}

function validateRawKey(rawKey) {
  const lines = rawKey.split(/\r?\n/).filter((line) => line.length > 0);
  if (!lines[0]?.startsWith(RAW_COMMENT_PREFIX)) {
    fail(
      'Invalid Tauri updater private key: missing the required "untrusted comment:" header. ' +
        'Use the private key generated by `npm run tauri signer generate -- --ci -w /secure/path/key`.',
    );
  }

  if (lines.length < 2) {
    fail(
      'Invalid Tauri updater private key: expected both the comment line and the encoded key payload. ' +
        'Do not store only the second line or the public key in `TAURI_SIGNING_PRIVATE_KEY`.',
    );
  }
}

function writeGithubOutput(name, value) {
  const outputPath = process.env.GITHUB_OUTPUT;
  if (!outputPath) {
    return;
  }

  writeFileSync(outputPath, `${name}=${value}\n`, { flag: 'a' });
}

function writeGithubEnv(name, value) {
  const envPath = process.env.GITHUB_ENV;
  if (!envPath) {
    return;
  }

  writeFileSync(envPath, `${name}<<EOF\n${value}\nEOF\n`, { flag: 'a' });
}

const input = process.env.TAURI_SIGNING_PRIVATE_KEY_INPUT;

if (!input?.trim()) {
  fail(
    'Missing `TAURI_SIGNING_PRIVATE_KEY` GitHub secret. Generate one with ' +
      '`npm run tauri signer generate -- --ci -w /secure/path/key` and store the full private key output in GitHub Secrets.',
  );
}

const rawKey = normalizeRawKey(input) ?? decodeBase64Key(input);

if (!rawKey) {
  fail(
    'Unable to parse `TAURI_SIGNING_PRIVATE_KEY`. Supported formats are: ' +
      'the single-line base64 value generated by `tauri signer generate --ci`, or the raw multi-line secret key text.',
  );
}

validateRawKey(rawKey);

const keyDir = mkdtempSync(join(tmpdir(), 'tauri-signing-key-'));
const keyPath = join(keyDir, 'updater.key');

writeFileSync(keyPath, `${rawKey}\n`, 'utf8');
writeGithubOutput('private_key_path', keyPath);
writeGithubEnv('TAURI_SIGNING_PRIVATE_KEY', rawKey);

console.log(`Prepared Tauri updater private key at ${keyPath}`);
